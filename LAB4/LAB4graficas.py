class Life(object):
    def __init__(self, screen):
        _, _, self.width, self.height = screen.get_rect()
        self.screen = screen

    def clear(self):
        self.screen.fill((0, 0, 0))

    def pixel(self, x, y, color):
        self.screen.set_at((x, y), color)

    def copy(self):
        self.preScreen = self.screen.copy()

    def render(self):
        for x in range(self.width):
            for y in range(self.height):
                celula = self.preScreen.get_at((x, y))
                vecinos = []
                vivo = 0
                muerto = 0

                try:
                    v1 = self.preScreen.get_at((x + 1, y))
                    vecinos.append(v1)                    
                except:
                    continue

                try:   
                    v2 = self.preScreen.get_at((x - 1, y))
                    vecinos.append(v2) 
                except:
                    continue
                
                try:
                    v3 = self.preScreen.get_at((x, y + 1))
                    vecinos.append(v3)  
                except:
                    continue
                
                try:
                    v4 = self.preScreen.get_at((x, y - 1))
                    vecinos.append(v4) 
                except:
                    continue
                
                try:
                    v5 = self.preScreen.get_at((x + 1, y + 1))
                    vecinos.append(v5) 
                except:
                    continue
                    
                try:
                    v6 = self.preScreen.get_at((x + 1, y - 1))
                    vecinos.append(v6) 
                except:
                    continue

                try:
                    v7 = self.preScreen.get_at((x - 1, y + 1))
                    vecinos.append(v7) 
                except:
                    continue
                
                try:
                    v8 = self.preScreen.get_at((x - 1, y - 1))
                    vecinos.append(v8) 
                except:
                    pass

                for vecino in vecinos:
                    if vecino[0] == 0:
                        muerto += 1
                    else:
                        vivo += 1

                if vivo < 2 and celula[0] == 255:
                    self.pixel(x, y, black)

                if (vivo == 2 or vivo == 3) and celula[0] == 255:
                    self.pixel(x, y, white)

                if vivo > 3 and celula[0] == 255:
                    self.pixel(x, y, black)

                if vivo == 3 and celula[0] == 0:
                    self.pixel(x, y, white)

    def Draw_matrix(self, x_inicial, y_inicial, matrix):
        y = y_inicial
        for row in matrix:
            x = x_inicial
            for item in row:
                if item == 1:
                    self.pixel(x, y, white)
                else:
                    self.pixel(x, y, black)

                x += 1
            y += 1




